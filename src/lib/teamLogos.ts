export function getTeamLogoUrl(teamName: string, sport: string): string | null {
  const sportUpper = sport?.toUpperCase() || '';
  let abbr: string | undefined;
  let sportPath: string;
  
  switch (sportUpper) {
    case 'NBA':
      abbr = NBA_TEAMS[teamName];
      sportPath = 'nba';
      break;
    case 'NFL':
      abbr = NFL_TEAMS[teamName];
      sportPath = 'nfl';
      break;
    case 'NHL':
      abbr = NHL_TEAMS[teamName];
      sportPath = 'nhl';
      break;
    case 'MLB':
      abbr = MLB_TEAMS[teamName];
      sportPath = 'mlb';
      break;
    default:
      return null;
  }
  
  if (!abbr) return null;
  
  return `https://a.espn.com/i/teamlogos/${sportPath}/500/${abbr}.png`;
}

